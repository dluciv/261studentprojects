using System;


namespace ClassFunction
{
    public class Functions
    {
        
        public static int addTwoNumbers(int oneNumber, int twoNumber)
        {
            int addition = oneNumber + twoNumber;
            return addition;
        }
        public static int deduct(int oneNumber, int twoNumber)
        {
            int deduction = oneNumber - twoNumber;
            return deduction;
        }
    }
}
